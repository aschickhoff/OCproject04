class Messages:
    @staticmethod
    def exit_message():
        print("")
        print("Have a nice day!")
        print("")

    @staticmethod
    def invalid_input():
        print("Oops...your given input was not valid. Please retry!")

    @staticmethod
    def invalid_option():
        print("Oops..Option not available yet!")

    @staticmethod
    def player_already_added():
        print("This player is already part of the tournament!")

    @staticmethod
    def tournament_created():
        print("The tournament was created!")

    @staticmethod
    def blank_line():
        print("")

    @staticmethod
    def create_tournament_first():
        print("Please create a tournament first!")

    @staticmethod
    def tournament_ended():
        print("The tournament is over!")
